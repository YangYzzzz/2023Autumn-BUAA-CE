package FrontEnd.NonTerminal;

public class Ident {
    private String name;
    private int line;
    public Ident() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLine() {
        return line;
    }

    public void setLine(int line) {
        this.line = line;
    }
}
